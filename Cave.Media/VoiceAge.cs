namespace Cave.Media
{
    /// <summary>
    /// Defines the values for the age of a synthesized voice.
    /// </summary>
    public enum VoiceAge
    {
        /// <summary>Undefined, system default will be used</summary>
        Undefined = 0,

        /// <summary>Indicates a child voice.</summary>
        Child,

        /// <summary>Indicates a teenage voice.</summary>
        Teen,

        /// <summary>Indicates an adult voice.</summary>
        Adult,

        /// <summary>Indicates a senior voice.</summary>
        Senior
    }
}
